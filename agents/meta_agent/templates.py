_TEMPLATE_INDEX: dict[str, dict] = {t["template_id"]: t for t in TEMPLATES}


class InstallRequest(BaseModel):
    """Request body for installing a template."""
    template_id: str
    user_email: str
    custom_name: Optional[str] = None


def _agents_path() -> str:
    """Return absolute path to memory/agents.jsonl."""
    base = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    return os.path.join(base, "memory", "agents.jsonl")


def _load_agents() -> list[dict]:
    """Load all agents from agents.jsonl."""
    path = _agents_path()
    if not os.path.exists(path):
        return []
    agents = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                agents.append(json.loads(line))
    return agents


def _save_agent(agent: dict) -> None:
    """Append a single agent record to agents.jsonl."""
    path = _agents_path()
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "a", encoding="utf-8") as f:
        f.write(json.dumps(agent) + "\n")


@router.get("/list")
def list_templates(category: Optional[str] = None) -> dict:
    """Return all templates without spec, optionally filtered by category."""
    results = []
    for t in TEMPLATES:
        if category and t["category"].lower() != category.lower():
            continue
        results.append({k: v for k, v in t.items() if k != "spec"})
    return {"templates": results, "total": len(results)}


@router.get("")
def list_templates_root(category: Optional[str] = None) -> dict:
    """GET /templates — 与 /templates/list 相同。"""
    return list_templates(category)


@router.get("/get/{template_id}")
def get_template(template_id: str) -> dict:
    """Return full template including spec."""
    tmpl = _TEMPLATE_INDEX.get(template_id)
    if not tmpl:
        raise HTTPException(status_code=404, detail=f"Template '{template_id}' not found.")
    return tmpl


@router.post("/install")
def install_template(body: InstallRequest) -> dict:
    """Install a template as an agent for the given user."""
    tmpl = _TEMPLATE_INDEX.get(body.template_id)
    if not tmpl:
        raise HTTPException(status_code=404, detail=f"Template '{body.template_id}' not found.")

    existing = _load_agents()
    for agent in existing:
        if (
            agent.get("user_email") == body.user_email
            and agent.get("source") == "template"
            and agent.get("template_id") == body.template_id
        ):
            raise HTTPException(
                status_code=409,
                detail=f"Template '{body.template_id}' already installed for {body.user_email}.",
            )

    agent_id = str(uuid.uuid4())
    agent = {
        "agent_id": agent_id,
        "user_email": body.user_email,
        "template_id": body.template_id,
        "name": body.custom_name or tmpl["name"],
        "category": tmpl["category"],
        "icon": tmpl["icon"],
        "color": tmpl["color"],
        "description": tmpl["description"],
        "spec": tmpl["spec"],
        "dna_score": 95.0,
        "grade": "A",
        "source": "template",
        "created_at": datetime.utcnow().isoformat(),
    }
    _save_agent(agent)
    return {"agent_id": agent_id, "message": f"Template installed as agent '{agent['name']}'."}


